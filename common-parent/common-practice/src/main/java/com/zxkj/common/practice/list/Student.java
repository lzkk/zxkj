package com.zxkj.common.practice.list;

public class Student {

    private String age;
    private Integer sex;

    public Student(String age, Integer sex) {
        this.age = age;
        this.sex = sex;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public Integer getSex() {
        return sex;
    }

    public void setSex(Integer sex) {
        this.sex = sex;
    }


}